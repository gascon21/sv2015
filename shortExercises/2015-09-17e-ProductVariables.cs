/*
Write a C# program to display the result of multiplying 15 by 12, 
using variables.
*/

public class ProductVariables
{
	public static void Main()
	{
		// Let's declare variables
		int x;
		int y;
		
		// Now, let's assign their values
		x = 15;
		y = 12;
		
		// And finally... let's calculate
		System.Console.WriteLine(x * y);
	}
}
